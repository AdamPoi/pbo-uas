    private void btnLogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogInActionPerformed
        if (!validateForm()) {
            return;
        }
        String hashPassword = AuthHelper.getMd5(txtPassword.getText());
        try {
            Guru guru = gs.login(txtNIP.getText(), hashPassword);
            if (guru == null) {
                JOptionPane.showMessageDialog(null, "Username dan Password Salah !");
                return;
            }
            AuthHelper.setCurrentUSer(guru);

        } catch (Exception e) {
        }
        this.dispose();
        ProfilGuru pg = new ProfilGuru();
        pg.show();
    }//GEN-LAST:event_btnLogInActionPerformed

